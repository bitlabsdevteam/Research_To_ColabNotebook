import { describe, it, expect, beforeAll } from "vitest";
import { PDFDocument } from "pdf-lib";
import fs from "fs";
import path from "path";

let FigureExtractorService: any;
let service: any;

beforeAll(async () => {
  const mod = await import(
    "../../apps/api/src/pdf-parser/figure-extractor.service"
  );
  FigureExtractorService = mod.FigureExtractorService;
  service = new FigureExtractorService();
});

describe("FigureExtractorService", () => {
  it("returns an empty array for a PDF with no images", async () => {
    // Create a text-only PDF
    const pdfDoc = await PDFDocument.create();
    const page = pdfDoc.addPage();
    page.drawText("No images here");
    const pdfBytes = await pdfDoc.save();
    const buffer = Buffer.from(pdfBytes);

    const figures = await service.extract(buffer);

    expect(Array.isArray(figures)).toBe(true);
    expect(figures).toHaveLength(0);
  });

  it("extracts embedded images as base64 with page numbers", async () => {
    // Create a PDF with an embedded JPEG image
    const pdfDoc = await PDFDocument.create();
    const page = pdfDoc.addPage();

    // Create a minimal 1x1 JPEG
    const jpegBytes = createMinimalJpeg();
    const jpegImage = await pdfDoc.embedJpg(jpegBytes);
    page.drawImage(jpegImage, { x: 50, y: 500, width: 100, height: 100 });

    const pdfBytes = await pdfDoc.save();
    const buffer = Buffer.from(pdfBytes);

    const figures = await service.extract(buffer);

    expect(figures.length).toBeGreaterThanOrEqual(1);
    expect(figures[0]).toHaveProperty("page");
    expect(figures[0]).toHaveProperty("base64");
    expect(typeof figures[0].page).toBe("number");
    expect(typeof figures[0].base64).toBe("string");
    expect(figures[0].base64.length).toBeGreaterThan(0);
  });

  it("returns figure objects with the correct interface", async () => {
    const pdfDoc = await PDFDocument.create();
    const page = pdfDoc.addPage();
    const jpegBytes = createMinimalJpeg();
    const jpegImage = await pdfDoc.embedJpg(jpegBytes);
    page.drawImage(jpegImage, { x: 50, y: 500, width: 100, height: 100 });

    const pdfBytes = await pdfDoc.save();
    const buffer = Buffer.from(pdfBytes);

    const figures = await service.extract(buffer);

    for (const fig of figures) {
      expect(fig).toHaveProperty("page");
      expect(fig).toHaveProperty("base64");
      // caption is optional
      if (fig.caption) {
        expect(typeof fig.caption).toBe("string");
      }
    }
  });

  it("handles invalid PDF gracefully", async () => {
    const buffer = Buffer.from("not a pdf");
    // Should not crash, either returns empty or throws
    await expect(service.extract(buffer)).rejects.toThrow();
  });
});

